var currentTrackIndex = 0;
var audio = document.getElementById('backgroundMusic');
var prevButton = document.getElementById('prevButton');
var playPauseButton = document.getElementById('playPauseButton');
var nextButton = document.getElementById('nextButton');
var musicSelect = document.getElementById('musicSelect');
var lyricsDisplay = document.getElementById('lyricsText');
var imageDisplay = document.getElementById('backgroundImage');

// Initialize background music, lyrics, and image
audio.src = musicTracks[currentTrackIndex];
lyricsDisplay.textContent = lyrics[currentTrackIndex];
imageDisplay.src = images[currentTrackIndex];

// Function to change background music, lyrics, and image
function changeBackground(direction) {
  // Pause current track if playing
  if (!audio.paused) {
    audio.pause();
    playPauseButton.textContent = 'Play';
  }

  // Determine next or previous track index
  if (direction === 'next') {
    currentTrackIndex = (currentTrackIndex + 1) % musicTracks.length;
  } else if (direction === 'prev') {
    currentTrackIndex = (currentTrackIndex - 1 + musicTracks.length) % musicTracks.length;
  }

  // Set new track, lyrics, and image
  audio.src = musicTracks[currentTrackIndex];
  lyricsDisplay.textContent = lyrics[currentTrackIndex];
  imageDisplay.src = images[currentTrackIndex];

  // Play the new track if previously playing
  if (!audio.paused) {
    audio.play();
    playPauseButton.textContent = 'Pause';
  }
}

// Event listeners for control buttons
prevButton.addEventListener('click', function () {
  changeBackground('prev');
});

playPauseButton.addEventListener('click', function () {
  if (audio.paused) {
    audio.play();
    playPauseButton.textContent = 'Pause';
  } else {
    audio.pause();
    playPauseButton.textContent = 'Play';
  }
});

nextButton.addEventListener('click', function () {
  changeBackground('next');
});

// Event listener for music selection dropdown
musicSelect.addEventListener('change', function () {
  currentTrackIndex = parseInt(musicSelect.value);
  audio.src = musicTracks[currentTrackIndex];
  lyricsDisplay.textContent = lyrics[currentTrackIndex];
  imageDisplay.src = images[currentTrackIndex];
  audio.play();
});

